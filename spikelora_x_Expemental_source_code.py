@torch.no_grad()
def predict_numpy(model, X, batch_size=128):
    loader = DataLoader(
        WindowDataset(X, np.zeros((len(X), 1), dtype=np.float32)),
        batch_size=batch_size,
        shuffle=False
    )

    preds = []

    model.eval()

    for xb, _ in loader:
        xb = _to_device(xb)
        reset_snn_state(model)
        pred = model(xb)
        preds.append(pred.detach().cpu())
        reset_snn_state(model)

    return torch.cat(preds, dim=0).numpy()


def topk_feature_masking_fidelity(
    model,
    Xte,
    Yte,
    feature_names,
    heatmap,
    top_k=3,
    random_repeats=5,
    batch_size=128,
    seed=0,
    mask_value=0.0
):
    """
    Tests whether the occlusion attribution is meaningful:
    - Mask top-k attributed features using mask_value. By default, mask_value
      is 0.0, which is the training-set mean after standardization.
    - Mask random-k features with the same procedure.
    - If top-k masking hurts more, the attribution has fidelity.
    """

    rng = np.random.default_rng(seed)

    original_preds = predict_numpy(model, Xte, batch_size=batch_size)
    original_rmse = rmse_np(Yte, original_preds)
    original_mae = mae_np(Yte, original_preds)
    original_smape = smape_np(Yte, original_preds)

    global_attr = heatmap.mean(axis=1)
    top_idx = np.argsort(global_attr)[::-1][:top_k]

    X_top = Xte.copy()
    X_top[:, :, top_idx] = mask_value

    top_preds = predict_numpy(model, X_top, batch_size=batch_size)
    top_rmse = rmse_np(Yte, top_preds)
    top_mae = mae_np(Yte, top_preds)
    top_smape = smape_np(Yte, top_preds)

    random_rmses = []
    random_maes = []
    random_smapes = []

    all_idx = np.arange(Xte.shape[-1])

    for _ in range(random_repeats):
        rand_idx = rng.choice(all_idx, size=top_k, replace=False)

        X_rand = Xte.copy()
        X_rand[:, :, rand_idx] = mask_value

        rand_preds = predict_numpy(model, X_rand, batch_size=batch_size)

        random_rmses.append(rmse_np(Yte, rand_preds))
        random_maes.append(mae_np(Yte, rand_preds))
        random_smapes.append(smape_np(Yte, rand_preds))

    random_rmse = float(np.mean(random_rmses))
    random_mae = float(np.mean(random_maes))
    random_smape = float(np.mean(random_smapes))

    fidelity = {
        "top_k": top_k,
        "top_features": ", ".join([feature_names[i] for i in top_idx]),
        "original_rmse": original_rmse,
        "topk_mask_rmse": top_rmse,
        "random_mask_rmse": random_rmse,
        "topk_rmse_increase": top_rmse - original_rmse,
        "random_rmse_increase": random_rmse - original_rmse,
        "fidelity_gain_rmse": (top_rmse - original_rmse) - (random_rmse - original_rmse),
        "original_mae": original_mae,
        "topk_mask_mae": top_mae,
        "random_mask_mae": random_mae,
        "original_smape": original_smape,
        "topk_mask_smape": top_smape,
        "random_mask_smape": random_smape,
    }

    return fidelity


def run_physics_violation_analysis(model, data, task_id, batch_size=128):
    """
    Post-hoc physical plausibility analysis.
    This does not change the model.
    """

    lower, upper = get_physical_limits_from_training(
        data["Ytr"],
        task_id=task_id,
        safety_factor=1.05
    )

    preds = predict_numpy(model, data["Xte"], batch_size=batch_size)

    violation_pct = count_physics_violations(
        preds,
        lower_limits=lower,
        upper_limits=upper
    )

    rmse = rmse_np(data["Yte"], preds)
    mae = mae_np(data["Yte"], preds)
    smape = smape_np(data["Yte"], preds)

    out = {
        "task": task_id,
        "rmse": rmse,
        "mae": mae,
        "smape": smape,
        "physical_violation_pct": violation_pct,
        "lower_limits": lower.tolist(),
        "upper_limits": upper.tolist()
    }

    return out


