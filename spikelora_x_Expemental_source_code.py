# Cell 12: Physics-Informed Regularization
# Corrected loss: compares preds with targets, not preds with preds
# ===============================

import torch
import torch.nn as nn


def physics_informed_loss(
    preds,
    targets,
    upper_limits=None,
    lower_limits=None,
    lambda_phys=0.01,
    base_loss_fn=None
):
    """
    Physics-informed regression loss.

    Loss = MSE(preds, targets)
           + lambda_phys * violation penalty

    upper_limits:
        Tensor/scalar with maximum physically allowed value.
        Example:
            - PV GHI upper bound: 1200 W/m2
            - Wind power upper bound: turbine rated power
            - Electricity consumption upper bound: historical operational max

    lower_limits:
        Tensor/scalar with minimum physically allowed value.
        Usually 0 for GHI, wind speed, wind power, and consumption.
    """

    if base_loss_fn is None:
        base_loss_fn = nn.MSELoss()

    base_loss = base_loss_fn(preds, targets)

    penalty = torch.tensor(0.0, device=preds.device)

    if upper_limits is not None:
        if not torch.is_tensor(upper_limits):
            upper_limits = torch.tensor(upper_limits, dtype=preds.dtype, device=preds.device)
        else:
            upper_limits = upper_limits.to(preds.device).type_as(preds)

        penalty = penalty + torch.clamp(preds - upper_limits, min=0.0).pow(2).mean()

    if lower_limits is not None:
        if not torch.is_tensor(lower_limits):
            lower_limits = torch.tensor(lower_limits, dtype=preds.dtype, device=preds.device)
        else:
            lower_limits = lower_limits.to(preds.device).type_as(preds)

        penalty = penalty + torch.clamp(lower_limits - preds, min=0.0).pow(2).mean()

    return base_loss + lambda_phys * penalty


def get_physical_limits_from_training(Ytr, task_id, safety_factor=1.05):
    """
    Builds simple operational physical limits from training targets.

    This is not a perfect physics model, but it is safe and practical:
    - lower bound = 0
    - upper bound = 1.05 * max training target per horizon

    For the paper, describe it as 'operational physical plausibility constraint'
    unless you use a true turbine/PV curve.
    """

    Ytr_np = np.asarray(Ytr)

    lower = np.zeros((1, Ytr_np.shape[1]), dtype=np.float32)
    upper = safety_factor * np.nanmax(Ytr_np, axis=0, keepdims=True).astype(np.float32)

    # Optional stronger fixed caps
    if task_id == "SR":
        # GHI rarely exceeds around 1200 W/m2 physically
        upper = np.minimum(upper, 1200.0)

    if task_id == "WS":
        # Wind speed lower bound already 0; upper remains data-derived
        pass

    if task_id == "WP":
        # Wind turbine active power cannot be negative.
        # Upper remains data-derived unless rated power is known.
        pass

    if task_id == "EC":
        # Consumption cannot be negative.
        pass

    return lower, upper


def count_physics_violations(preds, lower_limits=None, upper_limits=None):
    """
    Counts fraction of predictions violating physical/operational limits.
    """

    if torch.is_tensor(preds):
        p = preds.detach().cpu().numpy()
    else:
        p = np.asarray(preds)

    violations = np.zeros_like(p, dtype=bool)

    if lower_limits is not None:
        lo = np.asarray(lower_limits)
        violations |= p < lo

    if upper_limits is not None:
        hi = np.asarray(upper_limits)
        violations |= p > hi

    return 100.0 * violations.mean()

