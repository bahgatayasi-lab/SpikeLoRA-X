def train_one_epoch_physics(
    model,
    loader,
    optim,
    lower_limits,
    upper_limits,
    lambda_phys=0.01
):
    model.train()

    total = 0.0

    lower_t = torch.tensor(lower_limits, dtype=torch.float32, device=device)
    upper_t = torch.tensor(upper_limits, dtype=torch.float32, device=device)

    for xb, yb in loader:
        xb = _to_device(xb)
        yb = _to_device(yb)

        reset_snn_state(model)

        optim.zero_grad(set_to_none=True)

        pred = model(xb)

        loss = physics_informed_loss(
            preds=pred,
            targets=yb,
            lower_limits=lower_t,
            upper_limits=upper_t,
            lambda_phys=lambda_phys
        )

        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optim.step()

        total += loss.item() * xb.size(0)

        reset_snn_state(model)

    return total / len(loader.dataset)


def fit_physics(
    model,
    train_loader,
    val_loader,
    lower_limits,
    upper_limits,
    lr=1e-3,
    wd=1e-4,
    max_epochs=20,
    patience=7,
    lambda_phys=0.01
):
    optim = torch.optim.AdamW(
        [p for p in model.parameters() if p.requires_grad],
        lr=lr,
        weight_decay=wd
    )

    best_rmse = float("inf")
    best_state = None
    best_epoch = 0
    bad = 0

    for ep in range(1, max_epochs + 1):
        train_loss = train_one_epoch_physics(
            model=model,
            loader=train_loader,
            optim=optim,
            lower_limits=lower_limits,
            upper_limits=upper_limits,
            lambda_phys=lambda_phys
        )

        val_rmse, val_mae, val_smape = eval_model(model, val_loader)

        print(
            f"Epoch {ep}/{max_epochs} | "
            f"Physics Train Loss: {train_loss:.4f} | "
            f"Val RMSE: {val_rmse:.4f}"
        )

        if val_rmse < best_rmse:
            best_rmse = val_rmse
            best_state = {
                k: v.detach().cpu().clone()
                for k, v in model.state_dict().items()
            }
            best_epoch = ep
            bad = 0
        else:
            bad += 1

        if bad >= patience:
            print(f"Early stopping at epoch {ep}. Best epoch={best_epoch}")
            break

    if best_state is not None:
        model.load_state_dict(best_state)

    reset_snn_state(model)

    return model

