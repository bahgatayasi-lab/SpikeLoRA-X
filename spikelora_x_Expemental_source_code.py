def set_seed(seed=0):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)


def count_trainable_params(model):
    return sum(p.numel() for p in model.parameters() if p.requires_grad)


def count_total_params(model):
    return sum(p.numel() for p in model.parameters())


def _to_device(x):
    if torch.is_tensor(x):
        return x.float().to(device)
    return torch.from_numpy(x).float().to(device)


def reset_snn_state(model):
    try:
        functional.reset_net(model)
    except Exception:
        pass


def train_one_epoch(model, loader, optim):
    model.train()

    crit = nn.MSELoss()
    total = 0.0

    for xb, yb in loader:
        xb = _to_device(xb)
        yb = _to_device(yb)

        reset_snn_state(model)

        optim.zero_grad(set_to_none=True)

        pred = model(xb)

        loss = crit(pred, yb)
        loss.backward()

        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)

        optim.step()

        total += loss.item() * xb.size(0)

        reset_snn_state(model)

    return total / len(loader.dataset)


@torch.no_grad()
def eval_model(model, loader):
    model.eval()

    preds, trues = [], []

    for xb, yb in loader:
        xb = _to_device(xb)
        yb = _to_device(yb)

        reset_snn_state(model)

        pred = model(xb)

        preds.append(pred.detach())
        trues.append(yb.detach())

        reset_snn_state(model)

    preds = torch.cat(preds, dim=0)
    trues = torch.cat(trues, dim=0)

    m = eval_metrics(trues, preds)

    return m["rmse"], m["mae"], m["smape"]


def fit(model, train_loader, val_loader, lr=1e-3, wd=1e-4, max_epochs=30, patience=10):
    optim = torch.optim.AdamW(
        [p for p in model.parameters() if p.requires_grad],
        lr=lr,
        weight_decay=wd
    )

    best_rmse = float("inf")
    best_state = None
    best_epoch = 0
    bad = 0

    t0 = time.time()

    for ep in range(1, max_epochs + 1):
        train_loss = train_one_epoch(model, train_loader, optim)

        val_rmse, val_mae, val_smape = eval_model(model, val_loader)

        print(
            f"Epoch {ep}/{max_epochs} | "
            f"Train Loss: {train_loss:.4f} | "
            f"Val RMSE: {val_rmse:.4f}"
        )

        if val_rmse < best_rmse:
            best_rmse = val_rmse
            best_state = {
                k: v.detach().cpu().clone()
                for k, v in model.state_dict().items()
            }
            best_epoch = ep
            bad = 0
        else:
            bad += 1

        if bad >= patience:
            print(f"Early stopping at epoch {ep}. Best epoch={best_epoch}")
            break

    if best_state is not None:
        model.load_state_dict(best_state)

    reset_snn_state(model)

    return model, {
        "best_epoch": best_epoch,
        "best_val_rmse": best_rmse,
        "train_time_sec": time.time() - t0,
        "trainable_params": count_trainable_params(model),
        "total_params": count_total_params(model),
    }

