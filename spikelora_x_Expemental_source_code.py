class MLPBaseline(nn.Module):
    def __init__(self, lookback, F, K, hidden=256, dropout=0.1):
        super().__init__()
        self.net = nn.Sequential(
            nn.Flatten(),
            nn.Linear(lookback * F, hidden),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(hidden, hidden),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(hidden, K),
        )

    def forward(self, x):
        return self.net(x)


class RNNBaseline(nn.Module):
    def __init__(self, kind, F, K, hidden=128, layers=2, dropout=0.1):
        super().__init__()

        RNN = nn.LSTM if kind == "lstm" else nn.GRU

        self.rnn = RNN(
            input_size=F,
            hidden_size=hidden,
            num_layers=layers,
            batch_first=True,
            dropout=dropout if layers > 1 else 0.0
        )

        self.head = nn.Linear(hidden, K)

    def forward(self, x):
        out, _ = self.rnn(x)
        return self.head(out[:, -1])


class TCNBaseline(nn.Module):
    def __init__(self, F, K, channels=128, levels=3, k=3, dropout=0.1):
        super().__init__()

        layers = []
        in_ch = F

        for i in range(levels):
            dilation = 2 ** i
            pad = (k - 1) * dilation

            layers += [
                nn.Conv1d(in_ch, channels, kernel_size=k, dilation=dilation, padding=pad),
                nn.ReLU(),
                nn.Dropout(dropout),
            ]

            in_ch = channels

        self.net = nn.Sequential(*layers)
        self.head = nn.Linear(channels, K)

    def forward(self, x):
        z = x.transpose(1, 2)
        z = self.net(z)
        z = z[:, :, -1]
        return self.head(z)


class PatchTST(nn.Module):
    def __init__(
        self,
        F,
        K,
        patch_len=16,
        stride=8,
        d_model=128,
        nhead=4,
        layers=3,
        dropout=0.1
    ):
        super().__init__()

        self.patch_len = patch_len
        self.stride = stride

        self.proj = nn.Linear(patch_len * F, d_model)

        enc = nn.TransformerEncoderLayer(
            d_model=d_model,
            nhead=nhead,
            dim_feedforward=4 * d_model,
            dropout=dropout,
            batch_first=True,
            activation="gelu",
            norm_first=True
        )

        self.tf = nn.TransformerEncoder(enc, num_layers=layers)
        self.head = nn.Sequential(
            nn.LayerNorm(d_model),
            nn.Linear(d_model, K)
        )

    def patchify(self, x):
        B, L, Fdim = x.shape
        patches = []

        for s in range(0, L - self.patch_len + 1, self.stride):
            patches.append(x[:, s:s + self.patch_len, :])

        return torch.stack(patches, dim=1)

    def forward(self, x):
        p = self.patchify(x)
        B, Np, Pl, Fdim = p.shape

        tok = p.reshape(B, Np, Pl * Fdim)
        z = self.proj(tok)

        h = self.tf(z)

        return self.head(h[:, -1])


class Chomp1d(nn.Module):
    def __init__(self, chomp_size: int):
        super().__init__()
        self.chomp_size = chomp_size

    def forward(self, x):
        if self.chomp_size == 0:
            return x
        return x[:, :, :-self.chomp_size].contiguous()


class TemporalBlock(nn.Module):
    def __init__(self, c_in, c_out, kernel=3, dilation=1, dropout=0.1):
        super().__init__()

        pad = (kernel - 1) * dilation

        self.conv1 = nn.utils.weight_norm(
            nn.Conv1d(
                c_in,
                c_out,
                kernel_size=kernel,
                stride=1,
                padding=pad,
                dilation=dilation
            )
        )
        self.chomp1 = Chomp1d(pad)
        self.relu1 = nn.ReLU()
        self.drop1 = nn.Dropout(dropout)

        self.conv2 = nn.utils.weight_norm(
            nn.Conv1d(
                c_out,
                c_out,
                kernel_size=kernel,
                stride=1,
                padding=pad,
                dilation=dilation
            )
        )
        self.chomp2 = Chomp1d(pad)
        self.relu2 = nn.ReLU()
        self.drop2 = nn.Dropout(dropout)

        self.downsample = nn.Conv1d(c_in, c_out, kernel_size=1) if c_in != c_out else None
        self.relu = nn.ReLU()

        nn.init.kaiming_normal_(self.conv1.weight)
        nn.init.kaiming_normal_(self.conv2.weight)

        if self.downsample is not None:
            nn.init.kaiming_normal_(self.downsample.weight)

    def forward(self, x):
        out = self.conv1(x)
        out = self.chomp1(out)
        out = self.relu1(out)
        out = self.drop1(out)

        out = self.conv2(out)
        out = self.chomp2(out)
        out = self.relu2(out)
        out = self.drop2(out)

        res = x if self.downsample is None else self.downsample(x)

        return self.relu(out + res)


class SpikeEncoder(nn.Module):
    def __init__(self, thr=0.05):
        super().__init__()
        self.thr = thr

    def forward(self, x):
        dx = x[:, 1:, :] - x[:, :-1, :]
        sp = (dx.abs() > self.thr).float()
        z0 = torch.zeros_like(x[:, :1, :])
        return torch.cat([z0, sp], dim=1)


class SpikingTCN(nn.Module):
    """
    Strong original SpikingTCN used in the old procedure.
    """

    def __init__(self, F, K, channels=(64, 64, 64), T=8, thr=1.0):
        super().__init__()

        self.T = T

        self.encoder = SpikeEncoder(thr=0.05)
        self.in_proj = nn.Linear(F, channels[0])

        self.lif = neuron.LIFNode(
            v_threshold=thr,
            surrogate_function=surrogate.Sigmoid(),
            detach_reset=True
        )

        blocks = []
        c_in = channels[0]

        for i, c_out in enumerate(channels):
            blocks.append(
                TemporalBlock(
                    c_in,
                    c_out,
                    kernel=3,
                    dilation=2 ** i,
                    dropout=0.1
                )
            )
            c_in = c_out

        self.tcn = nn.Sequential(*blocks)
        self.head = nn.Linear(c_in, K)

    def forward(self, x):
        functional.reset_net(self)

        spk = self.encoder(x)

        acc = 0.0

        for _ in range(self.T):
            z = self.in_proj(spk)
            s = self.lif(z)
            h = self.tcn(s.transpose(1, 2))[:, :, -1]
            acc = acc + self.head(h)

        return acc / self.T


class LoRALinear(nn.Module):
    def __init__(self, base: nn.Linear, r=8, alpha=16, dropout=0.0):
        super().__init__()

        self.base = base
        self.r = r
        self.alpha = alpha
        self.scale = alpha / r
        self.drop = nn.Dropout(dropout)

        in_dim = base.in_features
        out_dim = base.out_features

        self.A = nn.Parameter(torch.zeros(r, in_dim))
        self.B = nn.Parameter(torch.zeros(out_dim, r))

        nn.init.kaiming_uniform_(self.A, a=math.sqrt(5))
        nn.init.zeros_(self.B)

        for p in self.base.parameters():
            p.requires_grad = False

    def forward(self, x):
        base_out = self.base(x)

        a = self.drop(x) @ self.A.t()
        delta = a @ self.B.t()

        return base_out + self.scale * delta


class SpikeLoRALinear(nn.Module):
    def __init__(self, base: nn.Linear, r=8, alpha=16, Vtheta=0.05, dropout=0.0):
        super().__init__()

        self.base = base
        self.r = r
        self.alpha = alpha
        self.scale = alpha / r
        self.drop = nn.Dropout(dropout)

        in_dim = base.in_features
        out_dim = base.out_features

        self.A = nn.Parameter(torch.zeros(r, in_dim))
        self.B = nn.Parameter(torch.zeros(out_dim, r))

        nn.init.kaiming_uniform_(self.A, a=math.sqrt(5))
        nn.init.zeros_(self.B)

        for p in self.base.parameters():
            p.requires_grad = False

        self.gate = neuron.LIFNode(
            v_threshold=Vtheta,
            surrogate_function=surrogate.Sigmoid(),
            detach_reset=True
        )

        self.last_sparsity_pct = None

    def forward(self, x):
        functional.reset_net(self.gate)

        base_out = self.base(x)

        a = self.drop(x) @ self.A.t()
        g = self.gate(a)
        a_tilde = g * a

        with torch.no_grad():
            self.last_sparsity_pct = float((g == 0).float().mean().item() * 100.0)

        delta = a_tilde @ self.B.t()

        return base_out + self.scale * delta


def freeze_module(m: nn.Module):
    for p in m.parameters():
        p.requires_grad = False


def unfreeze_module(m: nn.Module):
    for p in m.parameters():
        p.requires_grad = True


class SpikingTCN_Adapters(nn.Module):
    def __init__(self, base_model: nn.Module, peft="lora", r=8, alpha=16, Vtheta=0.05):
        super().__init__()

        assert hasattr(base_model, "head"), "SpikingTCN must have .head"

        self.base = base_model
        self.peft = peft

        base_head = self.base.head

        if peft == "lora":
            self.base.head = LoRALinear(base_head, r=r, alpha=alpha, dropout=0.0)
        elif peft == "spikelora":
            self.base.head = SpikeLoRALinear(base_head, r=r, alpha=alpha, Vtheta=Vtheta, dropout=0.0)
        else:
            raise ValueError("peft must be 'lora' or 'spikelora'")

        freeze_module(self.base)
        unfreeze_module(self.base.head)

        for m in self.base.modules():
            if isinstance(m, nn.LayerNorm):
                for p in m.parameters():
                    p.requires_grad = True

    def forward(self, x):
        return self.base(x)

    def get_spikelora_sparsity(self):
        if hasattr(self.base.head, "last_sparsity_pct"):
            return self.base.head.last_sparsity_pct
        return None


